#include "../minishell.h"

int is_empty(char *str)
{
    int i = 0;

    while(str[i])
    {
        if (!(str[i] == 32 || (str[i] >= 9 && str[i] <= 13)))
            return(0);
        i++;
    }
    return(1);
}
